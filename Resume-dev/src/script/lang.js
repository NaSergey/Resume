function translate(langCode) {
    const translatableElements = document.querySelectorAll('[class*="translate-"]');

    translatableElements.forEach((element) => {
        // Находим класс, начинающийся с "translate-"
        const classList = Array.from(element.classList);
        const translateClass = classList.find(cls => cls.startsWith('translate-'));

        if (translateClass) {
            // Извлекаем ключ после "translate-"
            // Например: "translate-workstart" -> "workstart"
            // "translate-workstart2" -> "workstart2"
            const translationKey = translateClass.replace('translate-', '');

            // Если есть перевод для этого ключа, меняем текст
            if (lang[translationKey] && lang[translationKey][langCode]) {
                element.textContent = lang[translationKey][langCode];
            }
        }
    });
}