function translate(langCode) {
    const translatableElements = document.querySelectorAll('[class^="translate-"]');
    
    translatableElements.forEach((element) => {
        const className = element.className;
        const key = className.split(' ')[0];  // Извлекаем класс, например "translate-Skills"
        const translationKey = key.split('-')[1]; // Извлекаем "Skills"

        // Если есть перевод для этого ключа, меняем текст
        if (lang[translationKey]) {
            element.textContent = lang[translationKey][langCode];
        }
    });
}